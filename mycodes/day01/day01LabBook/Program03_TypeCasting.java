package day1;

public class Program03_TypeCasting {
	public static void main(String[] args) {
		int intvar=5;
		long longvar=10;
		float floatvar=15.5f;
		double doublevar=20.25;
		char charvar='1';
		System.out.println("Integer to long "+(long)intvar);
		System.out.println("Integer to float "+(float)intvar);
		System.out.println("Integer to double "+(double)intvar);
		System.out.println("Integer to Character "+(char)intvar);
		System.out.println("Long to int "+(int)longvar);
		System.out.println("Long to float "+(float)longvar);
		System.out.println("Long to double "+(double)longvar);
		System.out.println("Long to Character "+(char)longvar);
		System.out.println("Float to int "+(int)floatvar);
		System.out.println("Float to long "+(long)floatvar);
		System.out.println("Float to double "+(double)floatvar);
		System.out.println("Float to Character "+(char)floatvar);
		System.out.println("Character to int "+(int)charvar);
		System.out.println("Character to long "+(long)charvar);
		System.out.println("Character to float "+(float)charvar);
		System.out.println("Character to double "+(double)charvar);
	}
}
